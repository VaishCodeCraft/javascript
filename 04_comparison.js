let a = 70;
let b = 30;

console.log(a == b);   // false (70 is not equal to 30)
console.log(a === b);  // false (70 is not strictly equal to 30)
console.log(a != b);   // true  (70 is not equal to 30)
console.log(a !== b);  // true  (70 is not strictly equal to 30)
console.log(a > b);    // true  (70 is greater than 30)
console.log(a < b);    // false (70 is not less than 30)
console.log(a >= b);   // true  (70 is greater than or equal to 30)
console.log(a <= b);   // false (70 is not less than or equal to 30)
